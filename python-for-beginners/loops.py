# Loop structures (27-28)

plants = ['monstera', 'suculenta', 'crossandra']

# For loop
for plant in plants:
    print(plant, '<3')
print()

for i in range(0, 2):
    print('Water the plants')
print()

#While loop
index = 0
while index < len(plants):
    print(plants[index], 'S2')
    index = index + 1
